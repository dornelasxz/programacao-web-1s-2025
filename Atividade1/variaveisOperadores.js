// declaração de variáveis e operações
let num1 = 10, num2 = 5;

// operação de adição
let soma = num1 + num2;
console.log(`Resultado da adição entre ${num1} e ${num2} é ${soma}`);

// operação de subtração
let subtracao = num1 - num2;
console.log(`Resultado da subtração entre ${num1} e ${num2} é ${subtracao}`);

// operação de multiplicação
let multiplicacao = num1 * num2;
console.log(`Resultado da multiplicação entre ${num1} e ${num2} é ${multiplicacao}`);

// operação de divisão
let divisao = num1 / num2;
console.log(`Resultado da divisão entre ${num1} e ${num2} é ${divisao}`);

// operação de módulo (resto da divisão)
let modulo = num1 % num2;
console.log(`Resultado do módulo entre ${num1} e ${num2} é ${modulo}`);

// operação de exponenciação
let potencia = num1 ** num2;
console.log(`Resultado da exponenciação entre ${num1} e ${num2} é ${potencia}`);
